def print_summary(results: dict) -> None:
    """Prints configuration summary."""
    clear()
    header()
    print("\n  SETUP COMPLETE\n")
    print("  " + "─" * 46)

    status_map = {
        True: "✅ Connected",
        False: "⚠️  Saved — verify credentials",
        "pending": "⏳ Pending — configure on company network",
        "configured": "✅ Configured",
    }

    for item, status in results.items():
        label = status_map.get(status, str(status))
        print(f"  {item:<20} {label}")

    print("  " + "─" * 46)
    print("\n  Run the system with:")
    print("  python -m src.cli\n")


# ------------------------------------------------------------
# Main wizard flow
# ------------------------------------------------------------

def main():
    clear()
    header()
    print("\n  Welcome! This wizard will configure your environment.")
    print("  You can re-run it anytime to update your settings.\n")

    # Check if .env already exists
    if Path(".env").exists():
        print("  An existing .env file was found.")
        reconfigure = ask("Reconfigure existing setup? (y/n)", "n")
        if reconfigure.lower() != "y":
            print("\n  Setup cancelled. Run 'python -m src.cli' to start.")
            return

    results = {}

    # Company
    pause()
    clear()
    header()
    company = setup_company()
    results["Company"] = "configured"

    # Jira
    pause()
    clear()
    header()
    jira_ok = setup_jira()
    results["Jira"] = jira_ok

    # Email
    pause()
    clear()
    header()
    email_ok = setup_email()
    results["Email"] = email_ok

    # Netsuite
    pause()
    clear()
    header()
    print("\n  Do you have your Netsuite credentials available?")
    has_netsuite = ask("Configure Netsuite now? (y/n)", "n")
    if has_netsuite.lower() == "y":
        setup_netsuite()
        results["Netsuite"] = "pending"
    else:
        write_env({"NETSUITE_MOCK": "true"})
        results["Netsuite"] = "pending"

    # Vendor
    pause()
    clear()
    header()
    print("\n  Do you have your vendor portal credentials available?")
    has_vendor = ask("Configure vendor portal now? (y/n)", "n")
    if has_vendor.lower() == "y":
        setup_vendor()
        results["Vendor portal"] = "pending"
    else:
        write_env({"VENDOR_MOCK": "true"})
        results["Vendor portal"] = "pending"

    # Slack / Teams
    write_env({
        "NOTIFY_SLACK": "false",
        "NOTIFY_TEAMS": "false",
        "SLACK_WEBHOOK_URL": "",
        "TEAMS_WEBHOOK_URL": "",
        "FINANCE_TEAM_EMAIL": "",
    })

    # Summary
    pause()
    print_summary(results)


if __name__ == "__main__":
    main()