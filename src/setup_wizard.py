# ------------------------------------------------------------
# Helpers
# ------------------------------------------------------------

def clear():
    os.system("cls" if os.name == "nt" else "clear")


def header():
    print("=" * 52)
    print("  IT Procurement Automation")
    print("  Setup Wizard")
    print("=" * 52)


def section(title: str):
    print(f"\n  ── {title} {'─' * (40 - len(title))}")


def ask(prompt: str, default: str = "") -> str:
    """Asks for input with an optional default value."""
    if default:
        value = input(f"  {prompt} [{default}]: ").strip()
        return value if value else default
    return input(f"  {prompt}: ").strip()


def ask_password(prompt: str) -> str:
    """Asks for a password — shown as input for simplicity."""
    return input(f"  {prompt}: ").strip()


def success(msg: str):
    print(f"\n  ✅ {msg}")


def warning(msg: str):
    print(f"\n  ⚠️  {msg}")


def error(msg: str):
    print(f"\n  ❌ {msg}")


def pause():
    input("\n  Press Enter to continue...")


# ------------------------------------------------------------
# Config updater
# ------------------------------------------------------------

def update_config(updates: dict, config_path: str = "config.yaml") -> None:
    """Updates specific fields in config.yaml."""
    with open(config_path, "r", encoding="utf-8") as f:
        cfg = yaml.safe_load(f)

    # Deep merge updates into config
    for section_key, values in updates.items():
        if section_key not in cfg:
            cfg[section_key] = {}
        if isinstance(values, dict):
            cfg[section_key].update(values)
        else:
            cfg[section_key] = values

    with open(config_path, "w", encoding="utf-8") as f:
        yaml.dump(cfg, f, allow_unicode=True, default_flow_style=False, sort_keys=False)


def write_env(values: dict, env_path: str = ".env") -> None:
    """Writes or updates variables in .env file."""
    existing = {}

    # Read existing values if file exists
    if Path(env_path).exists():
        with open(env_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    key, _, val = line.partition("=")
                    existing[key.strip()] = val.strip()

    # Merge new values
    existing.update(values)

    # Define section order
    sections = {
        "Jira": ["JIRA_URL", "JIRA_USER", "JIRA_API_TOKEN", "JIRA_MOCK"],
        "Netsuite": [
            "NETSUITE_ACCOUNT_ID", "NETSUITE_CONSUMER_KEY",
            "NETSUITE_CONSUMER_SECRET", "NETSUITE_TOKEN_ID",
            "NETSUITE_TOKEN_SECRET", "NETSUITE_MOCK"
        ],
        "Vendor": ["VENDOR_URL", "VENDOR_USER", "VENDOR_PASSWORD", "VENDOR_MOCK"],
        "Notifications": [
            "NOTIFY_EMAIL", "NOTIFY_EMAIL_SENDER", "NOTIFY_EMAIL_PASSWORD",
            "NOTIFY_SMTP_HOST", "NOTIFY_SMTP_PORT", "NOTIFY_MOCK",
            "NOTIFY_SLACK", "NOTIFY_TEAMS",
            "SLACK_WEBHOOK_URL", "TEAMS_WEBHOOK_URL", "FINANCE_TEAM_EMAIL"
        ],
    }

    with open(env_path, "w", encoding="utf-8") as f:
        f.write("# IT Procurement Automation - Environment Variables\n")
        f.write("# Generated by setup wizard\n\n")

        written = set()
        for section_name, keys in sections.items():
            section_values = {k: existing[k] for k in keys if k in existing}
            if section_values:
                f.write(f"# {section_name}\n")
                for key, val in section_values.items():
                    f.write(f"{key}={val}\n")
                    written.add(key)
                f.write("\n")

        # Write any remaining keys not in sections
        remaining = {k: v for k, v in existing.items() if k not in written}
        if remaining:
            f.write("# Other\n")
            for key, val in remaining.items():
                f.write(f"{key}={val}\n")


