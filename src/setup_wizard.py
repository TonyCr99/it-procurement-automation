def setup_company() -> dict:
    """Collects company information."""
    section("Company Information")
    print()

    name = ask("Company name", "Your Company Name")
    country = ask("Country code (e.g. MX, US, CO)", "MX")
    timezone = ask("Timezone", "America/Mexico_City")

    update_config({
        "company": {
            "name": name,
            "country": country,
            "timezone": timezone,
        }
    })

    success(f"Company configured: {name}")
    return {"name": name}


def setup_jira() -> bool:
    """Collects and tests Jira credentials."""
    section("Jira Configuration")
    print()
    print("  Get your API token at: id.atlassian.com/manage-profile/security/api-tokens\n")

    url = ask("Jira URL (e.g. https://yourcompany.atlassian.net)")
    user = ask("Jira email")
    token = ask_password("Jira API token")
    project_key = ask("Jira project key (e.g. IT-PURCHASE)")

    # Test connection
    print("\n  Testing Jira connection...")
    try:
        from jira import JIRA
        client = JIRA(server=url, basic_auth=(user, token))
        client.myself()
        success("Jira connected successfully.")
        connected = True
    except Exception as e:
        error(f"Could not connect to Jira: {e}")
        warning("Credentials saved but connection failed. Verify and re-run wizard.")
        connected = False

    write_env({
        "JIRA_URL": url,
        "JIRA_USER": user,
        "JIRA_API_TOKEN": token,
        "JIRA_MOCK": "false" if connected else "true",
    })

    update_config({
        "ticket": {
            "project_key": project_key,
        }
    })

    return connected


def setup_email() -> bool:
    """Collects and tests email credentials."""
    section("Email Notifications")
    print()
    print("  For Gmail: use an App Password, not your regular password.")
    print("  Generate at: myaccount.google.com/apppasswords\n")

    sender = ask("Sender email address")
    password = ask_password("Email password or App Password")
    smtp_host = ask("SMTP host", "smtp.gmail.com")
    smtp_port = ask("SMTP port", "587")
    test_recipient = ask("Send test email to")

    # Test connection
    print("\n  Sending test email...")
    try:
        msg = MIMEText(
            "This is a test email from IT Procurement Automation.\n\n"
            "If you received this, your email configuration is working correctly."
        )
        msg["Subject"] = "[IT Procurement] Setup Test"
        msg["From"] = sender
        msg["To"] = test_recipient

        with smtplib.SMTP(smtp_host, int(smtp_port)) as server:
            server.starttls()
            server.login(sender, password)
            server.sendmail(sender, test_recipient, msg.as_string())

        success(f"Test email sent to {test_recipient}")
        connected = True
    except Exception as e:
        error(f"Could not send email: {e}")
        warning("Credentials saved but email failed. Verify and re-run wizard.")
        connected = False

    write_env({
        "NOTIFY_EMAIL": "true",
        "NOTIFY_EMAIL_SENDER": sender,
        "NOTIFY_EMAIL_PASSWORD": password,
        "NOTIFY_SMTP_HOST": smtp_host,
        "NOTIFY_SMTP_PORT": smtp_port,
        "NOTIFY_MOCK": "false" if connected else "true",
    })

    return connected


